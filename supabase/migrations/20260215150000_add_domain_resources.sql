-- Phase 4: Add Domain Resources
-- Adds orders, vision_exams, and inventory tables with role-based RLS policies

-- ============================================================
-- 1. Create updated_at trigger function (generic for all tables)
-- ============================================================
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at := now();
  RETURN NEW;
END;
$$;

-- ============================================================
-- 2. Orders table
-- ============================================================
CREATE TABLE "public"."orders" (
  "id" bigint generated by default as identity not null,
  "contact_id" bigint not null references "public"."contacts"("id") on delete cascade,
  "member_id" bigint references "public"."members"("id"),
  "status" text not null default 'new_entry',
  "order_date" timestamp with time zone not null default now(),
  "expected_delivery" timestamp with time zone,
  "arrived_date" timestamp with time zone,
  "completed_date" timestamp with time zone,
  "pickup_date" timestamp with time zone,
  "frame_description" text,
  "lens_details" jsonb default '{}',
  "accessories" jsonb default '[]',
  "total_amount" numeric(10,2),
  "notes" text default '',
  "created_at" timestamp with time zone not null default now(),
  "updated_at" timestamp with time zone not null default now(),
  constraint orders_pkey primary key (id),
  constraint orders_status_check check (status in ('new_entry', 'lens_ordered', 'arrived', 'qa_check', 'in_progress', 'completed', 'notified', 'picked_up'))
);

ALTER TABLE "public"."orders" ENABLE ROW LEVEL SECURITY;

-- Set updated_at trigger
CREATE TRIGGER set_orders_updated_at
BEFORE UPDATE ON public.orders
FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

