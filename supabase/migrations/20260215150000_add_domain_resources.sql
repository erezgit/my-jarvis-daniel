-- ============================================================
-- 5. Orders summary view
-- ============================================================
CREATE OR REPLACE VIEW "public"."orders_summary" WITH (security_invoker=off) AS
SELECT
  o.*,
  c.first_name as contact_first_name,
  c.last_name as contact_last_name,
  m.first_name as member_first_name,
  m.last_name as member_last_name
FROM "public"."orders" o
LEFT JOIN "public"."contacts" c ON o.contact_id = c.id
LEFT JOIN "public"."members" m ON o.member_id = m.id;

-- ============================================================
-- 6. Auto-set member_id triggers
-- ============================================================

-- Orders: auto-set member_id to current member
CREATE OR REPLACE FUNCTION public.set_order_member_id_default()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = ''
AS $$
BEGIN
  IF NEW.member_id IS NULL THEN
    NEW.member_id := (SELECT id FROM public.members WHERE user_id = auth.uid() LIMIT 1);
  END IF;
  RETURN NEW;
END;
$$;

CREATE TRIGGER set_order_member_id
BEFORE INSERT ON public.orders
FOR EACH ROW EXECUTE FUNCTION public.set_order_member_id_default();

