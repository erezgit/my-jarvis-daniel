-- ============================================================
-- 7. RLS Policies
-- ============================================================

-- === ORDERS ===

-- SELECT: admin/manager/office see all, salesperson sees own, glazier sees assigned
CREATE POLICY "orders_select" ON "public"."orders"
AS PERMISSIVE FOR SELECT TO authenticated
USING (
  public.get_current_member_role() IN ('admin', 'manager', 'office')
  OR (public.get_current_member_role() = 'salesperson' AND member_id = public.get_current_member_id())
  OR (public.get_current_member_role() = 'glazier' AND status IN ('in_progress', 'qa_check'))
);

-- INSERT: admin/manager/office/salesperson
CREATE POLICY "orders_insert" ON "public"."orders"
AS PERMISSIVE FOR INSERT TO authenticated
WITH CHECK (
  public.get_current_member_role() IN ('admin', 'manager', 'office', 'salesperson')
);

-- UPDATE: admin/manager see all, salesperson own only
CREATE POLICY "orders_update" ON "public"."orders"
AS PERMISSIVE FOR UPDATE TO authenticated
USING (
  public.get_current_member_role() IN ('admin', 'manager')
  OR (public.get_current_member_role() = 'salesperson' AND member_id = public.get_current_member_id())
);

-- DELETE: admin/manager only
CREATE POLICY "orders_delete" ON "public"."orders"
AS PERMISSIVE FOR DELETE TO authenticated
USING (
  public.get_current_member_role() IN ('admin', 'manager')
);

